// Install dependencies
async function installDependencies() {
  console.log('\nüì¶ Installing dependencies...');
  
  try {
    // Server dependencies
    console.log('   Installing server dependencies...');
    execSync('npm install', { cwd: config.serverPath, stdio: 'inherit' });
    
    // Client dependencies
    console.log('   Installing client dependencies...');
    execSync('npm install', { cwd: config.clientPath, stdio: 'inherit' });
    
    console.log('‚úÖ All dependencies installed');
    
  } catch (error) {
    console.log('‚ùå Error installing dependencies:', error.message);
    throw error;
  }
}

// Setup database with Prisma
async function setupDatabase() {
  console.log('\nüóÑÔ∏è  Setting up database...');
  
  try {
    // Generate Prisma client
    console.log('   Generating Prisma client...');
    execSync('npx prisma generate', { cwd: config.serverPath, stdio: 'inherit' });
    
    // Run migrations
    console.log('   Running database migrations...');
    execSync('npx prisma migrate deploy', { cwd: config.serverPath, stdio: 'inherit' });
    
    // Seed database
    console.log('   Seeding database...');
    execSync('npm run db:seed:prod', { cwd: config.serverPath, stdio: 'inherit' });
    
    console.log('‚úÖ Database setup completed');
    
  } catch (error) {
    console.log('‚ùå Error setting up database:', error.message);
    console.log('\nüîß Manual commands:');
    console.log('   cd server');
    console.log('   npx prisma generate');
    console.log('   npx prisma migrate deploy');
    console.log('   npm run db:seed:prod');
    throw error;
  }
}

// Create necessary directories
function createDirectories() {
  console.log('\nüìÅ Creating necessary directories...');
  
  const directories = [
    path.join(config.serverPath, 'logs'),
    path.join(config.serverPath, 'uploads'),
    path.join(config.serverPath, 'auth_info_baileys'),
    path.join(config.serverPath, 'qr-codes'),
    path.join(config.serverPath, 'ssl'),
    path.join(__dirname, '..', 'auth_info_baileys'),
    path.join(__dirname, '..', 'qr-codes')
  ];
  
  directories.forEach(dir => {
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
      console.log(`   ‚úÖ Created: ${path.basename(dir)}`);
    } else {
      console.log(`   ‚è≠Ô∏è  Exists: ${path.basename(dir)}`);
    }
  });
}

// Build client
async function buildClient() {
  console.log('\nüèóÔ∏è  Building client...');
  
  try {
    execSync('npm run build', { cwd: config.clientPath, stdio: 'inherit' });
    console.log('‚úÖ Client build completed');
    
  } catch (error) {
    console.log('‚ùå Error building client:', error.message);
    console.log('   Client will run in development mode');
  }
}

// Start services
async function startServices() {
  console.log('\nüöÄ Starting services...');
  
  try {
    // Start server in background
    console.log('   Starting backend server...');
    const serverProcess = spawn('npm', ['run', 'start:production'], {
      cwd: config.serverPath,
      detached: true,
      stdio: 'ignore'
    });
    
    serverProcess.unref();
    console.log('   ‚úÖ Backend server started (PID: ' + serverProcess.pid + ')');
    
    // Wait a moment for server to start
    await new Promise(resolve => setTimeout(resolve, 3000));
    
    // Start WhatsApp Bot
    console.log('   Starting WhatsApp bot...');
    const whatsappProcess = spawn('node', ['scripts/whatsapp-bot-integrated.js'], {
      cwd: path.join(__dirname, '..'),
      detached: true,
      stdio: 'ignore',
      env: { 
        ...process.env, 
        NODE_ENV: 'production',
        NEXT_PUBLIC_API_URL: config.backendUrl,
        NEXT_PUBLIC_WS_URL: config.backendUrl.replace('http', 'ws')
      }
    });
    
    whatsappProcess.unref();
    console.log('   ‚úÖ WhatsApp bot started (PID: ' + whatsappProcess.pid + ')');
    
    // Wait a moment for WhatsApp bot to initialize
    await new Promise(resolve => setTimeout(resolve, 2000));
    
    // Start client
    console.log('   Starting frontend client...');
    const clientProcess = spawn('npm', ['run', 'start'], {
      cwd: config.clientPath,
      detached: true,
      stdio: 'ignore'
    });
    
    clientProcess.unref();
    console.log('   ‚úÖ Frontend client started (PID: ' + clientProcess.pid + ')');
    
    console.log('\nüéâ All services started successfully!');
    console.log('\nüìã Service URLs:');
    console.log(`   Frontend: ${config.frontendUrl}`);
    console.log(`   Backend API: ${config.backendUrl}`);
    console.log(`   Prisma Studio: http://localhost:5555`);
    
    console.log('\nüì± WhatsApp Bot:');
    console.log('   ‚úÖ WhatsApp bot is running independently');
    console.log('   üìÅ QR Code location: ./qr-codes/whatsapp-qr.png');
    console.log(`   üåê Web access: ${config.backendUrl}/qr/whatsapp-qr.png`);
    console.log('   üì± Scan QR code with WhatsApp to connect');
    console.log('   ü§ñ Bot commands: /menu, /daftar, /jobs, /myjobs, /stats');
    
    console.log('\nüîß Management Commands:');
    console.log('   View server logs: tail -f server/logs/app.log');
    console.log('   View WhatsApp status: cat scripts/whatsapp-status.json');
    console.log('   Stop all services: pkill -f "npm run start" && pkill -f "whatsapp-bot-integrated"');
    console.log('   Database studio: cd server && npm run db:studio');
    console.log('   Restart WhatsApp bot: node scripts/whatsapp-bot-integrated.js');
    
  } catch (error) {
    console.log('‚ùå Error starting services:', error.message);
  }
}

// Main execution
async function main() {
  try {
    console.log('Starting production setup...\n');
    
    // Check prerequisites
    if (!checkPostgreSQL()) {
      console.log('\n‚ùå PostgreSQL is required. Please install it first.');
      console.log('   Ubuntu/Debian: sudo apt install postgresql postgresql-contrib');
      console.log('   CentOS/RHEL: sudo yum install postgresql-server postgresql-contrib');
      console.log('   Windows: Download from https://www.postgresql.org/download/windows/');
      console.log('   macOS: brew install postgresql');
      process.exit(1);
    }
    
    // Get user preferences
    const serverIp = await askQuestion(`Server IP address (default: ${config.serverIp}): `);
    if (serverIp) {
      config.serverIp = serverIp;
      config.frontendUrl = `http://${serverIp}:3000`;
      config.backendUrl = `http://${serverIp}:3001`;
    }
    
    const frontendUrl = await askQuestion(`Frontend URL (default: ${config.frontendUrl}): `);
    if (frontendUrl) config.frontendUrl = frontendUrl;
    
    const backendUrl = await askQuestion(`Backend URL (default: ${config.backendUrl}): `);
    if (backendUrl) config.backendUrl = backendUrl;
    
    // Setup steps
    await setupPostgreSQL();
    createDirectories();
    createEnvFile();
    await installDependencies();
    await setupDatabase();
    await buildClient();
    
    // Confirm before starting services
    const startServicesConfirm = await askQuestion('\nStart all services now? (Y/n): ');
    if (startServicesConfirm.toLowerCase() !== 'n') {
      await startServices();
    } else {
      console.log('\n‚úÖ Setup completed! Start services manually with:');
      console.log('   cd server && npm run start:production');
      console.log('   cd client && npm run start');
    }
    
    console.log('\nüéâ Production setup completed successfully!');
    console.log('\nüìñ Documentation: POSTGRESQL-SETUP.md');
    console.log('üîß Troubleshooting: Check server/logs/app.log');
    
  } catch (error) {
    console.log('\n‚ùå Setup failed:', error.message);
    console.log('\nüîß Manual setup required. See POSTGRESQL-SETUP.md for details.');
  } finally {
    rl.close();
  }
}

// Run if called directly
if (require.main === module) {
  main().catch(console.error);
}

module.exports = { 
  setupPostgreSQL, 
  createEnvFile, 
  installDependencies, 
  setupDatabase, 
  startServices 
};
